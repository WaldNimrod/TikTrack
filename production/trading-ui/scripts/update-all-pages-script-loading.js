/**
 * Update all pages with correct script loading code
 */


// ===== FUNCTION INDEX =====

// === Script Loading ===
// - updateAllPagesScriptLoading() - Update All Pages Script Loading
// - validateScriptDependencies() - Validate Script Dependencies
// - loadRequiredScripts() - Load Required Scripts

const fs = require('fs');
const path = require('path');

// Load the generator function
const generatorPath = path.join(__dirname, 'generate-script-loading-code.js');
let generateScriptLoadingCode;
try {
  const generatorModule = require(generatorPath);
  generateScriptLoadingCode = generatorModule.generateScriptLoadingCode;
} catch (e) {
  console.error('❌ Could not load generator:', e.message);
  process.exit(1);
}

// Get all HTML files in trading-ui
const tradingUiPath = path.join(__dirname, '..');
const htmlFiles = fs.readdirSync(tradingUiPath)
  .filter(file => file.endsWith('.html'))
  .map(file => ({
    filename: file,
    path: path.join(tradingUiPath, file),
    pageName: file.replace('.html', '')
  }));

console.log(`📋 Found ${htmlFiles.length} HTML files\n`);

// Load page configs to check which pages have configs
const configsPath = path.join(__dirname, 'page-initialization-configs.js');
const configsContent = fs.readFileSync(configsPath, 'utf8');
const vm = require('vm');
const context = { 
  window: {}, 
  PAGE_CONFIGS: {}, 
  ADDITIONAL_PAGE_CONFIGS: {},
  Object: Object,
  module: {}, 
  exports: {}, 
  require: require, 
  __dirname, 
  __filename: configsPath,
  console: console
};
vm.createContext(context);
vm.runInContext(configsContent, context);
// Merge ADDITIONAL_PAGE_CONFIGS into PAGE_CONFIGS if needed
let PAGE_CONFIGS = context.window.PAGE_CONFIGS || context.PAGE_CONFIGS || {};
if (context.ADDITIONAL_PAGE_CONFIGS && Object.keys(context.ADDITIONAL_PAGE_CONFIGS).length > 0) {
  PAGE_CONFIGS = { ...PAGE_CONFIGS, ...context.ADDITIONAL_PAGE_CONFIGS };
}
// Also check if Object.assign was used
if (context.window && context.window.PAGE_CONFIGS) {
  PAGE_CONFIGS = context.window.PAGE_CONFIGS;
}

let updated = 0;
let skipped = 0;
let errors = [];

htmlFiles.forEach(({ filename, path: filePath, pageName }) => {
  try {
    // Check if page has config
    if (!PAGE_CONFIGS[pageName]) {
      console.log(`⏭️  Skipping ${filename} - no page config found`);
      skipped++;
      return;
    }

    // Read HTML file
    const htmlContent = fs.readFileSync(filePath, 'utf8');
    
    // Check if file already has generated code
    const forceUpdate = process.argv.includes('--force');
    if (htmlContent.includes('Generated by generate-script-loading-code.js') && !forceUpdate) {
      console.log(`✅ ${filename} already has generated code (use --force to update)`);
      return;
    }

    // Generate new script loading code
    const newScriptCode = generateScriptLoadingCode(pageName);
    if (!newScriptCode) {
      console.log(`⚠️  Could not generate code for ${filename}`);
      skipped++;
      return;
    }

    // Find the script section to replace
    // Look for pattern: <!-- ===== START SCRIPT LOADING ORDER ===== --> or <!-- ===== SCRIPT LOADING ORDER ===== -->
    const scriptStartPattern = /<!--\s*=====\s*(START\s+)?SCRIPT\s*LOADING\s*ORDER\s*=====\s*-->/i;
    const scriptEndPattern = /<!--\s*=====\s*TESTING\s*SCRIPTS|<!--\s*=====\s*EXTERNAL\s*LIBRARIES|<!--\s*=====\s*FINAL\s*SUMMARY|<\/body>/i;
    
    const startMatch = htmlContent.search(scriptStartPattern);
    if (startMatch === -1) {
      // Try to find any script tag after Bootstrap
      const bootstrapMatch = htmlContent.indexOf('bootstrap');
      if (bootstrapMatch !== -1) {
        // Find first script tag after bootstrap
        const firstScriptAfterBootstrap = htmlContent.indexOf('<script', bootstrapMatch);
        if (firstScriptAfterBootstrap !== -1) {
          // Find the comment before it or use the script tag itself
          const commentBefore = htmlContent.lastIndexOf('<!--', firstScriptAfterBootstrap);
          if (commentBefore !== -1 && commentBefore > bootstrapMatch - 100) {
            console.log(`⚠️  ${filename} - Found scripts but no standard header, will insert at script start`);
            // We'll insert before the first script
            const insertPos = commentBefore;
            const endPos = htmlContent.indexOf('</body>', insertPos);
            if (endPos !== -1) {
              // Extract external libs and testing scripts
              const externalLibsMatch = htmlContent.indexOf('<!-- ===== EXTERNAL LIBRARIES', insertPos);
              const testingScriptsMatch = htmlContent.indexOf('<!-- ===== TESTING SCRIPTS', insertPos);
              
              let externalLibs = '';
              let testingScripts = '';
              
              if (externalLibsMatch !== -1 && externalLibsMatch < endPos) {
                const externalEnd = htmlContent.indexOf('</script>', externalLibsMatch) + 9;
                externalLibs = htmlContent.substring(externalLibsMatch, externalEnd);
              }
              
              if (testingScriptsMatch !== -1 && testingScriptsMatch < endPos) {
                const testingEnd = htmlContent.indexOf('</script>', testingScriptsMatch) + 9;
                testingScripts = htmlContent.substring(testingScriptsMatch, testingEnd);
              }
              
              const beforeScripts = htmlContent.substring(0, insertPos);
              const afterScripts = htmlContent.substring(endPos);
              
              const newContent = beforeScripts + 
                newScriptCode + 
                (externalLibs ? '\n    ' + externalLibs : '') +
                (testingScripts ? '\n    ' + testingScripts : '') +
                afterScripts;
              
              fs.writeFileSync(filePath, newContent, 'utf8');
              console.log(`✅ Updated ${filename} (inserted at script start)`);
              updated++;
              return;
            }
          }
        }
      }
      console.log(`⚠️  Could not find script section start in ${filename}`);
      skipped++;
      return;
    }

    // Find the end - look for </body> or external libraries section
    let endMatch = htmlContent.indexOf('</body>', startMatch);
    if (endMatch === -1) {
      endMatch = htmlContent.length;
    }

    // Find external libraries and testing scripts sections
    const externalLibsMatch = htmlContent.indexOf('<!-- ===== EXTERNAL LIBRARIES', startMatch);
    const testingScriptsMatch = htmlContent.indexOf('<!-- ===== TESTING SCRIPTS', startMatch);
    
    let endPos = endMatch;
    if (externalLibsMatch !== -1 && externalLibsMatch < endPos) {
      endPos = externalLibsMatch;
    } else if (testingScriptsMatch !== -1 && testingScriptsMatch < endPos) {
      endPos = testingScriptsMatch;
    }

    // Extract external libraries and testing scripts
    let externalLibs = '';
    let testingScripts = '';
    
    if (externalLibsMatch !== -1) {
      const externalEnd = htmlContent.indexOf('</script>', externalLibsMatch) + 9;
      externalLibs = htmlContent.substring(externalLibsMatch, externalEnd);
    }
    
    if (testingScriptsMatch !== -1) {
      const testingEnd = htmlContent.indexOf('</script>', testingScriptsMatch) + 9;
      testingScripts = htmlContent.substring(testingScriptsMatch, testingEnd);
    }

    // Replace the script section
    const beforeScripts = htmlContent.substring(0, startMatch);
    const afterScripts = htmlContent.substring(endPos);
    
    const newContent = beforeScripts + 
      newScriptCode + 
      (externalLibs ? '\n    ' + externalLibs : '') +
      (testingScripts ? '\n    ' + testingScripts : '') +
      afterScripts;

    // Write updated file
    fs.writeFileSync(filePath, newContent, 'utf8');
    console.log(`✅ Updated ${filename}`);
    updated++;
  } catch (error) {
    console.error(`❌ Error updating ${filename}:`, error.message);
    errors.push({ filename, error: error.message });
  }
});

console.log(`\n📊 Summary:`);
console.log(`   ✅ Updated: ${updated}`);
console.log(`   ⏭️  Skipped: ${skipped}`);
console.log(`   ❌ Errors: ${errors.length}`);

if (errors.length > 0) {
  console.log(`\n❌ Errors:`);
  errors.forEach(({ filename, error }) => {
    console.log(`   - ${filename}: ${error}`);
  });
}

