const LoadOrderValidator = require('./load-order-validator');
const fs = require('fs');
const path = require('path');
const vm = require('vm');

const scriptsDir = path.join(__dirname, '../../trading-ui/scripts');
const tradingUiDir = path.join(__dirname, '../../trading-ui');
const manifestPath = path.join(scriptsDir, 'init-system/package-manifest.js');

// Load manifest using LoadOrderValidator
async function loadManifest() {
  const validator = new LoadOrderValidator(scriptsDir, tradingUiDir);
  return await validator.loadManifest();
}

// Generate script tags for a page
function generateScriptTags(pageName, packages, manifest) {
  let html = '';
  html += '    <!-- =============================================================== -->\n';
  html += '    <!-- ===== START SCRIPT LOADING ORDER ===== -->\n';
  html += '    <!-- =============================================================== -->\n';
  html += '    <!-- ⚠️ DO NOT MODIFY MANUALLY - Generated by generate-script-loading-code.js -->\n';
  html += '    <!-- 📋 Loading Order: BASE → SERVICES → MODULES → UI-ADVANCED → CRUD → PREFERENCES → INIT-SYSTEM -->\n';
  html += `    <!-- 🎯 Page: ${pageName} | Generated: ${new Date().toISOString()} -->\n`;
  html += `    <!-- 📦 Packages: ${packages.join(', ')} -->\n\n`;
  
  const sortedPackages = packages
    .map(pkgName => manifest[pkgName])
    .filter(pkg => pkg)
    .sort((a, b) => (a.loadOrder || 0) - (b.loadOrder || 0));
  
  let scriptCounter = 1;
  
  sortedPackages.forEach((pkg, pkgIndex) => {
    html += `    <!-- =============================================================== -->\n`;
    html += `    <!-- ===== PACKAGE ${pkgIndex + 1}/${sortedPackages.length}: ${pkg.name.toUpperCase()} ===== -->\n`;
    html += `    <!-- =============================================================== -->\n`;
    html += `    <!-- Load Order: ${pkg.loadOrder} | Description: ${pkg.description} -->\n`;
    html += `    <!-- Dependencies: ${pkg.dependencies ? pkg.dependencies.join(', ') : 'none'} -->\n`;
    html += `    <!-- Critical: ${pkg.critical ? 'YES' : 'NO'} | Version: ${pkg.version} -->\n\n`;
    
    const sortedScripts = (pkg.scripts || [])
      .filter(script => script.required !== false)
      .sort((a, b) => (a.loadOrder || 0) - (b.loadOrder || 0));
    
    sortedScripts.forEach((script) => {
      html += `    <!-- [${scriptCounter}] Load Order: ${scriptCounter} -->\n`;
      const scriptSrc = script.file.startsWith('http://') || script.file.startsWith('https://') 
        ? script.file 
        : `scripts/${script.file}?v=1.0.0`;
      html += `    <script src="${scriptSrc}"></script> <!-- ${script.description} -->\n`;
      scriptCounter++;
    });
    html += '\n';
  });
  
  html += '    <!-- =============================================================== -->\n';
  html += '    <!-- ===== PAGE-SPECIFIC SCRIPTS ===== -->\n';
  html += '    <!-- =============================================================== -->\n';
  
  return html;
}

// Update HTML file
function updateHTMLFile(htmlPath, pageName, packages, manifest) {
  const content = fs.readFileSync(htmlPath, 'utf8');
  
  // Use regex patterns from update-all-pages-script-loading.js
  const scriptStartPattern = /<!--\s*=====\s*(START\s+)?SCRIPT\s*LOADING\s*ORDER\s*=====\s*-->/i;
  const scriptEndPattern = /<!--\s*=====\s*TESTING\s*SCRIPTS|<!--\s*=====\s*EXTERNAL\s*LIBRARIES|<!--\s*=====\s*FINAL\s*SUMMARY|<!--\s*=====\s*PAGE-SPECIFIC\s*SCRIPTS|<\/body>/i;
  
  const startMatch = content.search(scriptStartPattern);
  if (startMatch === -1) {
    return false;
  }
  
  // Find the end - look for </body> or other markers
  let endMatch = content.search(scriptEndPattern);
  if (endMatch === -1) {
    endMatch = content.indexOf('</body>', startMatch);
    if (endMatch === -1) {
      endMatch = content.length;
    }
  }
  
  // Find external libraries and testing scripts sections
  const externalLibsMatch = content.indexOf('<!-- ===== EXTERNAL LIBRARIES', startMatch);
  const testingScriptsMatch = content.indexOf('<!-- ===== TESTING SCRIPTS', startMatch);
  
  let endPos = endMatch;
  if (externalLibsMatch !== -1 && externalLibsMatch < endPos) {
    endPos = externalLibsMatch;
  } else if (testingScriptsMatch !== -1 && testingScriptsMatch < endPos) {
    endPos = testingScriptsMatch;
  }
  
  // Extract external libraries and testing scripts
  let externalLibs = '';
  let testingScripts = '';
  
  if (externalLibsMatch !== -1 && externalLibsMatch < endPos) {
    const externalEnd = content.indexOf('</script>', externalLibsMatch) + 9;
    if (externalEnd > externalLibsMatch) {
      externalLibs = content.substring(externalLibsMatch, externalEnd);
    }
  }
  
  if (testingScriptsMatch !== -1 && testingScriptsMatch < endPos) {
    const testingEnd = content.indexOf('</script>', testingScriptsMatch) + 9;
    if (testingEnd > testingScriptsMatch) {
      testingScripts = content.substring(testingScriptsMatch, testingEnd);
    }
  }
  
  const newScriptTags = generateScriptTags(pageName, packages, manifest);
  const beforeScripts = content.substring(0, startMatch);
  const afterScripts = content.substring(endPos);
  
  const newContent = beforeScripts + 
    newScriptTags + 
    (externalLibs ? '\n    ' + externalLibs : '') +
    (testingScripts ? '\n    ' + testingScripts : '') +
    afterScripts;
  
  fs.writeFileSync(htmlPath, newContent, 'utf8');
  return true;
}

// Load configs using the working method from update-all-pages-script-loading.js
function loadConfigsWorking() {
  const configsPath = path.join(scriptsDir, 'page-initialization-configs.js');
  const configsContent = fs.readFileSync(configsPath, 'utf8');
  const vm = require('vm');
  const context = { 
    window: {}, 
    pageInitializationConfigs: {}, 
    additionalPageInitializationConfigs: {},
    Object: Object,
    module: {}, 
    exports: {}, 
    require: require, 
    __dirname: path.dirname(configsPath), 
    __filename: configsPath,
    console: console
  };
  vm.createContext(context);
  vm.runInContext(configsContent, context);
  // Merge additionalPageInitializationConfigs into pageInitializationConfigs if needed
  let pageInitializationConfigs = context.window.pageInitializationConfigs || context.pageInitializationConfigs || {};
  if (context.additionalPageInitializationConfigs && Object.keys(context.additionalPageInitializationConfigs).length > 0) {
    pageInitializationConfigs = { ...pageInitializationConfigs, ...context.additionalPageInitializationConfigs };
  }
  // Also check if Object.assign was used
  if (context.window && context.window.pageInitializationConfigs) {
    pageInitializationConfigs = context.window.pageInitializationConfigs;
  }
  return pageInitializationConfigs;
}

// Main
async function main() {
  console.log('🔧 תיקון סדר טעינה לכל העמודים...\n');
  
  const validator = new LoadOrderValidator(scriptsDir, tradingUiDir);
  const manifest = await loadManifest();
  const configs = loadConfigsWorking();
  const htmlFiles = validator.getAllHTMLFiles();
  
  let updated = 0;
  let skipped = 0;
  const errors = [];
  
  htmlFiles.forEach(htmlFile => {
    const pageName = validator.getPageName(htmlFile);
    const config = configs[pageName] || configs[pageName + '.html'];
    
    if (!config || !config.packages) {
      skipped++;
      if (config) {
        console.log(`⏭️  ${pageName}: אין packages`);
      } else {
        console.log(`⏭️  ${pageName}: אין config`);
      }
      return;
    }
    
    const packages = config.packages;
    // htmlFile from validator is already a full path
    const htmlPath = htmlFile;
    
    try {
      if (updateHTMLFile(htmlPath, pageName, packages, manifest)) {
        updated++;
        console.log(`✅ ${pageName}`);
      } else {
        skipped++;
      }
    } catch (error) {
      errors.push({ page: pageName, error: error.message });
      console.error(`❌ ${pageName}: ${error.message}`);
    }
  });
  
  console.log('\n═══════════════════════════════════════════════════════════');
  console.log('📊 סיכום');
  console.log('═══════════════════════════════════════════════════════════\n');
  console.log(`✅ עודכנו: ${updated}`);
  console.log(`⏭️  דולגו: ${skipped}`);
  console.log(`❌ שגיאות: ${errors.length}`);
  
  if (errors.length > 0) {
    console.log('\nשגיאות:');
    errors.forEach(e => {
      console.log(`  - ${e.page}: ${e.error}`);
    });
  }
}

main().catch(err => {
  console.error('❌ שגיאה:', err);
  process.exit(1);
});

