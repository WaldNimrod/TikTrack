/**
 * Fix Load Order for All Pages
 * ==============================
 * 
 * Regenerates script loading code for all HTML pages
 * to match the updated package manifest (modules loadOrder: 2.5)
 * 
 * @version 1.0.0
 * @created November 2025
 */

const fs = require('fs');
const path = require('path');
const glob = require('glob');
const vm = require('vm');

const tradingUiDir = path.join(__dirname, '../../trading-ui');
const manifestPath = path.join(tradingUiDir, 'scripts/init-system/package-manifest.js');
const configsPath = path.join(tradingUiDir, 'scripts/page-initialization-configs.js');

// Load manifest
function loadManifest() {
  const content = fs.readFileSync(manifestPath, 'utf8');
  const context = {
    module: {},
    exports: {},
    require: require,
    __dirname: path.dirname(manifestPath),
    window: {},
    PACKAGE_MANIFEST: {}
  };
  vm.createContext(context);
  vm.runInContext(content, context);
  return context.PACKAGE_MANIFEST || {};
}

// Load configs
function loadConfigs() {
  const content = fs.readFileSync(configsPath, 'utf8');
  const context = {
    module: {},
    exports: {},
    require: require,
    __dirname: path.dirname(configsPath),
    window: { PAGE_CONFIGS: {} },
    PAGE_CONFIGS: {}
  };
  vm.createContext(context);
  
  // Wrap in IIFE
  const wrappedContent = `(function() {
    ${content}
    return window.PAGE_CONFIGS || PAGE_CONFIGS || {};
  })()`;
  
  try {
    const result = vm.runInContext(wrappedContent, context);
    return result || context.PAGE_CONFIGS || context.window.PAGE_CONFIGS || {};
  } catch (e) {
    // Try alternative method
    const match = content.match(/PAGE_CONFIGS\s*=\s*({[\s\S]*?});/);
    if (match) {
      try {
        const extractedContext = { PAGE_CONFIGS: {} };
        vm.createContext(extractedContext);
        vm.runInContext(`PAGE_CONFIGS = ${match[1]};`, extractedContext);
        return extractedContext.PAGE_CONFIGS || {};
      } catch (e2) {
        console.warn('Could not parse PAGE_CONFIGS:', e2.message);
        return {};
      }
    }
    return {};
  }
}

// Generate script tags for a page
function generateScriptTags(pageName, packages, manifest) {
  let html = '';
  html += '    <!-- =============================================================== -->\n';
  html += '    <!-- ===== START SCRIPT LOADING ORDER ===== -->\n';
  html += '    <!-- =============================================================== -->\n';
  html += '    <!-- ⚠️ DO NOT MODIFY MANUALLY - Generated by generate-script-loading-code.js -->\n';
  html += '    <!-- 📋 Loading Order: BASE → SERVICES → MODULES → UI-ADVANCED → CRUD → PREFERENCES → INIT-SYSTEM -->\n';
  html += `    <!-- 🎯 Page: ${pageName} | Generated: ${new Date().toISOString()} -->\n`;
  html += `    <!-- 📦 Packages: ${packages.join(', ')} -->\n\n`;
  
  const sortedPackages = packages
    .map(pkgName => manifest[pkgName])
    .filter(pkg => pkg)
    .sort((a, b) => (a.loadOrder || 0) - (b.loadOrder || 0));
  
  let scriptCounter = 1;
  
  sortedPackages.forEach((pkg, pkgIndex) => {
    html += `    <!-- =============================================================== -->\n`;
    html += `    <!-- ===== PACKAGE ${pkgIndex + 1}/${sortedPackages.length}: ${pkg.name.toUpperCase()} ===== -->\n`;
    html += `    <!-- =============================================================== -->\n`;
    html += `    <!-- Load Order: ${pkg.loadOrder} | Description: ${pkg.description} -->\n`;
    html += `    <!-- Dependencies: ${pkg.dependencies ? pkg.dependencies.join(', ') : 'none'} -->\n`;
    html += `    <!-- Critical: ${pkg.critical ? 'YES' : 'NO'} | Version: ${pkg.version} -->\n\n`;
    
    const sortedScripts = (pkg.scripts || [])
      .filter(script => script.required !== false)
      .sort((a, b) => (a.loadOrder || 0) - (b.loadOrder || 0));
    
    sortedScripts.forEach((script) => {
      html += `    <!-- [${scriptCounter}] Load Order: ${scriptCounter} -->\n`;
      const scriptSrc = script.file.startsWith('http://') || script.file.startsWith('https://') 
        ? script.file 
        : `scripts/${script.file}?v=1.0.0`;
      html += `    <script src="${scriptSrc}"></script> <!-- ${script.description} -->\n`;
      scriptCounter++;
    });
    html += '\n';
  });
  
  html += '    <!-- =============================================================== -->\n';
  html += '    <!-- ===== PAGE-SPECIFIC SCRIPTS ===== -->\n';
  html += '    <!-- =============================================================== -->\n';
  
  return html;
}

// Update HTML file
function updateHTMLFile(htmlPath, pageName, packages, manifest) {
  const content = fs.readFileSync(htmlPath, 'utf8');
  
  // Find script loading section
  const startMarker = '<!-- ===== START SCRIPT LOADING ORDER ===== -->';
  let endMarker = '<!-- ===== END SCRIPT LOADING ORDER ===== -->';
  
  const startIndex = content.indexOf(startMarker);
  let endIndex = content.indexOf(endMarker);
  
  // If END marker not found, look for PAGE-SPECIFIC SCRIPTS or other markers
  if (endIndex === -1) {
    endMarker = '<!-- ===== PAGE-SPECIFIC SCRIPTS ===== -->';
    endIndex = content.indexOf(endMarker);
  }
  
  // If still not found, look for init-system scripts
  if (endIndex === -1) {
    const initSystemIndex = content.indexOf('scripts/init-system/package-manifest.js', startIndex);
    if (initSystemIndex !== -1) {
      // Find the end of the last script tag after init-system
      const afterInit = content.substring(initSystemIndex);
      const lastScriptMatch = afterInit.match(/<\/script>\s*\n\s*<!--/);
      if (lastScriptMatch) {
        endIndex = initSystemIndex + lastScriptMatch.index + lastScriptMatch[0].length;
      }
    }
  }
  
  if (startIndex === -1) {
    console.warn(`⚠️  ${pageName}: לא נמצאה סקציית טעינת סקריפטים`);
    return false;
  }
  
  if (endIndex === -1) {
    console.warn(`⚠️  ${pageName}: לא נמצא סוף סקציית טעינת סקריפטים`);
    return false;
  }
  
  const newScriptTags = generateScriptTags(pageName, packages, manifest);
  // Keep everything after PAGE-SPECIFIC SCRIPTS
  const afterSection = content.substring(endIndex);
  const newContent = content.substring(0, startIndex) + newScriptTags + afterSection;
  
  fs.writeFileSync(htmlPath, newContent, 'utf8');
  return true;
}

// Main
async function main() {
  console.log('🔧 תיקון סדר טעינה לכל העמודים...\n');
  
  const manifest = loadManifest();
  const configs = loadConfigs();
  
  const htmlFiles = glob.sync('trading-ui/**/*.html', { 
    ignore: ['**/node_modules/**', '**/mockups/**'] 
  });
  
  let updated = 0;
  let skipped = 0;
  const errors = [];
  
  htmlFiles.forEach(htmlFile => {
    const pageName = path.basename(htmlFile, '.html');
    const config = configs[pageName] || configs[pageName + '.html'];
    
    if (!config || !config.packages) {
      skipped++;
      return;
    }
    
    const packages = config.packages;
    const htmlPath = path.join(__dirname, '../../', htmlFile);
    
    try {
      if (updateHTMLFile(htmlPath, pageName, packages, manifest)) {
        updated++;
        console.log(`✅ ${pageName}`);
      } else {
        skipped++;
      }
    } catch (error) {
      errors.push({ page: pageName, error: error.message });
      console.error(`❌ ${pageName}: ${error.message}`);
    }
  });
  
  console.log('\n═══════════════════════════════════════════════════════════');
  console.log('📊 סיכום');
  console.log('═══════════════════════════════════════════════════════════\n');
  console.log(`✅ עודכנו: ${updated}`);
  console.log(`⏭️  דולגו: ${skipped}`);
  console.log(`❌ שגיאות: ${errors.length}`);
  
  if (errors.length > 0) {
    console.log('\nשגיאות:');
    errors.forEach(e => {
      console.log(`  - ${e.page}: ${e.error}`);
    });
  }
}

main().catch(err => {
  console.error('❌ שגיאה:', err);
  process.exit(1);
});

