const fs = require('fs');
const path = require('path');

/**
 * Detect environment from workspace directory name
 * - TikTrackApp-Production → production
 * - TikTrackApp → development
 * @returns {string} 'production' or 'development'
 */
function detectEnvironment() {
  const cwd = process.cwd();
  const workspaceName = path.basename(cwd);

  // Check for Production in directory name (case-insensitive)
  if (workspaceName.includes('Production') || workspaceName.includes('production')) {
    // TikTrackApp-Production should use testing environment
    if (workspaceName === 'TikTrackApp-Production') {
      return 'testing';
    }
    return 'production';
  } else if (workspaceName === 'TikTrackApp') {
    return 'development';
  } else {
    // Default to development if uncertain (safer default)
    console.warn(`⚠️ Could not determine environment from directory: ${workspaceName}`);
    console.warn('Defaulting to development. Use --mode flag to override.');
    return 'development';
  }
}

// Load package manifest
const manifestPath = path.join(__dirname, 'init-system', 'package-manifest.js');
let PACKAGE_MANIFEST = {};

try {
  // Try to require the module
  const manifestModule = require(manifestPath);
  if (manifestModule && manifestModule.PACKAGE_MANIFEST) {
    PACKAGE_MANIFEST = manifestModule.PACKAGE_MANIFEST;
  }
} catch (e) {
  console.error('⚠️ Could not load package manifest via require, trying eval...');
  // Fallback to eval
  const manifestContent = fs.readFileSync(manifestPath, 'utf8');
  // Create a safe context
  const vm = require('vm');
  const context = { module: {}, exports: {}, require: require, __dirname, __filename: manifestPath };
  vm.createContext(context);
  vm.runInContext(manifestContent, context);
  if (context.module.exports && context.module.exports.PACKAGE_MANIFEST) {
    PACKAGE_MANIFEST = context.module.exports.PACKAGE_MANIFEST;
  }
}

// Load page configs - this file doesn't export, so we need to parse it
const configsPath = path.join(__dirname, 'page-initialization-configs.js');
let PAGE_CONFIGS = {};

try {
  const configsContent = fs.readFileSync(configsPath, 'utf8');
  // Extract PAGE_CONFIGS from the file
  const vm = require('vm');
  const context = { window: {}, PAGE_CONFIGS: {}, module: {}, exports: {}, require: require, __dirname, __filename: configsPath };
  vm.createContext(context);
  vm.runInContext(configsContent, context);
  if (context.PAGE_CONFIGS && Object.keys(context.PAGE_CONFIGS).length > 0) {
    PAGE_CONFIGS = context.PAGE_CONFIGS;
  } else if (context.window && context.window.PAGE_CONFIGS) {
    PAGE_CONFIGS = context.window.PAGE_CONFIGS;
  }
} catch (e) {
  console.error('⚠️ Could not load page configs:', e.message);
}

/**
 * Generate script loading code for a page
 * @param {string} pageName - Page name
 * @param {string} mode - 'development' or 'production' (auto-detected if null)
 * @param {boolean} useBundles - Whether to use bundles (auto-set based on mode if null)
 */
function generateScriptLoadingCode(pageName, mode = null, useBundles = null) {
  // Auto-detect environment if not provided
  if (!mode) {
    mode = detectEnvironment();
  }
  
  // Auto-set useBundles based on environment
  // Production and testing use bundles, development doesn't
  if (useBundles === null) {
    useBundles = (mode === 'production' || mode === 'testing');
  }
  const pageConfig = PAGE_CONFIGS[pageName];
  if (!pageConfig) {
    console.error(`❌ No config found for page: ${pageName}`);
    return null;
  }

  const packages = pageConfig.packages || ['base'];
  
  // Determine if we should use bundles
  // Production and testing use bundles, development doesn't
  const shouldUseBundles = useBundles && (mode === 'production' || mode === 'testing');
  
  let html = '';
  html += '    <!-- =============================================================== -->\n';
  html += '    <!-- ===== START SCRIPT LOADING ORDER ===== -->\n';
  html += '    <!-- =============================================================== -->\n';
  html += '    <!-- ⚠️ DO NOT MODIFY MANUALLY - Generated by generate-script-loading-code.js -->\n';
  html += '    <!-- 📋 Loading Order: BASE → SERVICES → MODULES → UI-ADVANCED → CRUD → PREFERENCES → INIT-SYSTEM -->\n';
  html += `    <!-- 🎯 Page: ${pageName} | Generated: ${new Date().toISOString()} -->\n`;
  html += `    <!-- 📦 Packages: ${packages.join(', ')} -->\n`;
  html += `    <!-- 🔧 Mode: ${mode} | Use Bundles: ${shouldUseBundles} -->\n\n`;
  
  const sortedPackages = packages
    .map(pkgName => PACKAGE_MANIFEST[pkgName])
    .filter(pkg => pkg)
    .sort((a, b) => (a.loadOrder || 0) - (b.loadOrder || 0));
  
  let scriptCounter = 1;
  const loadedScripts = new Set();
  
  sortedPackages.forEach((pkg, pkgIndex) => {
    html += `    <!-- =============================================================== -->\n`;
    html += `    <!-- ===== PACKAGE ${pkgIndex + 1}/${sortedPackages.length}: ${pkg.name.toUpperCase()} ===== -->\n`;
    html += `    <!-- =============================================================== -->\n`;
    html += `    <!-- Load Order: ${pkg.loadOrder} | Description: ${pkg.description} -->\n`;
    html += `    <!-- Dependencies: ${pkg.dependencies ? pkg.dependencies.join(', ') : 'none'} -->\n`;
    html += `    <!-- Critical: ${pkg.critical ? 'YES' : 'NO'} | Version: ${pkg.version} -->\n\n`;
    
    // Sort scripts within package by loadOrder
    // Include all scripts - required and optional (required: false) are both needed
    // The package is included in pageConfig.packages, so all its scripts should be loaded
    const sortedScripts = (pkg.scripts || [])
      .sort((a, b) => (a.loadOrder || 0) - (b.loadOrder || 0));
    
    // Check if bundle exists for this package
    const bundlePath = path.join(__dirname, 'bundles', `${pkg.id}.bundle.js`);
    const bundleExists = shouldUseBundles && fs.existsSync(bundlePath);
    
    if (bundleExists && sortedScripts.length > 0) {
      // Use bundle for the entire package
      const loadingStrategy = pkg.loadingStrategy || 'defer';
      html += `    <!-- [${scriptCounter}] Package Bundle: ${pkg.id}.bundle.js | Strategy: ${loadingStrategy} -->\n`;
      
      const bundleSrc = `scripts/bundles/${pkg.id}.bundle.js?v=1.0.0`;
      let scriptTag;

      // Add initialization guard if specified
      if (pkg.initializationGuard) {
        const funcName = `checkAndLoad${pkg.id.charAt(0).toUpperCase() + pkg.id.slice(1)}`;
        html += `    <script>\n`;
        html += `      // Initialization guard for ${pkg.name}\n`;
        html += `      function ${funcName}() {\n`;
        html += `        if (${pkg.initializationGuard}) {\n`;
        html += `          const script = document.createElement('script');\n`;
        html += `          script.src = '${bundleSrc}';\n`;
        html += `          script.${loadingStrategy} = true;\n`;
        html += `          document.head.appendChild(script);\n`;
        html += `        } else {\n`;
        html += `          setTimeout(${funcName}, 10);\n`;
        html += `        }\n`;
        html += `      }\n`;
        html += `      ${funcName}();\n`;
        html += `    </script>\n`;
        scriptTag = `    <!-- Bundle ${pkg.id} will be loaded dynamically after guard check -->\n`;
      } else {
        if (loadingStrategy === 'sync') {
          scriptTag = `    <script src="${bundleSrc}"></script> <!-- ${pkg.name} bundle -->\n`;
        } else {
          scriptTag = `    <script src="${bundleSrc}" ${loadingStrategy}></script> <!-- ${pkg.name} bundle -->\n`;
        }
      }
      html += scriptTag;
      scriptCounter++;
      
      // Mark local scripts as loaded, but external scripts (CDN) need to be loaded separately
      sortedScripts.forEach(script => {
        if (!(script.file.startsWith('http://') || script.file.startsWith('https://'))) {
          loadedScripts.add(script.file);
        }
      });
      
      // Load external scripts (CDN) separately - they're not bundled
      sortedScripts.forEach((script) => {
        if (script.file.startsWith('http://') || script.file.startsWith('https://')) {
          const scriptLoadingStrategy = pkg.loadingStrategy || 'defer';
          html += `    <!-- [${scriptCounter}] External Script: ${script.file} | Strategy: ${scriptLoadingStrategy} -->\n`;
          
          let externalScriptTag;
          if (scriptLoadingStrategy === 'sync') {
            externalScriptTag = `    <script src="${script.file}"></script> <!-- ${script.description} -->\n`;
          } else {
            externalScriptTag = `    <script src="${script.file}" ${scriptLoadingStrategy}></script> <!-- ${script.description} -->\n`;
          }
          html += externalScriptTag;
          scriptCounter++;
        }
      });
    } else {
      // Use individual scripts (development mode or bundle doesn't exist)
      sortedScripts.forEach((script) => {
        // Include all scripts (required and optional) - they're in the package for a reason
        // Get loading strategy from script or package (default: defer for critical packages)
        const loadingStrategy = script.loadingStrategy || pkg.loadingStrategy || 'defer';
        
        html += `    <!-- [${scriptCounter}] Load Order: ${scriptCounter} | Strategy: ${loadingStrategy} -->\n`;
        
        // Handle external URLs (CDN) vs local files
        const scriptSrc = script.file.startsWith('http://') || script.file.startsWith('https://') 
          ? script.file 
          : `scripts/${script.file}?v=1.0.0`;
        
        // Generate script tag with loading strategy
        let scriptTag;
        if (loadingStrategy === 'sync') {
          // Synchronous loading (rare - only if explicitly needed)
          scriptTag = `    <script src="${scriptSrc}"></script> <!-- ${script.description} -->\n`;
        } else {
          // Async or defer loading
          scriptTag = `    <script src="${scriptSrc}" ${loadingStrategy}></script> <!-- ${script.description} -->\n`;
        }
        
        html += scriptTag;
        if (!(script.file.startsWith('http://') || script.file.startsWith('https://'))) {
          loadedScripts.add(script.file);
        }
        scriptCounter++;
      });
    }
    html += '\n';
  });
  
  html += '    <!-- =============================================================== -->\n';
  html += '    <!-- ===== PAGE-SPECIFIC SCRIPTS ===== -->\n';
  html += '    <!-- =============================================================== -->\n\n';
  
  // First, add the main page-specific script (e.g., cash_flows.js, alerts.js, trades.js)
  // This is the primary script file for each page
  // NOTE: When using bundles, page-specific scripts are included in bundles and should not be loaded separately
  if (!shouldUseBundles) {
    const mainPageScript = `${pageName}.js`;
    // __dirname is trading-ui/scripts, so the script is in the same directory
    const mainPageScriptPath = path.join(__dirname, mainPageScript);
    if (fs.existsSync(mainPageScriptPath) && !loadedScripts.has(mainPageScript)) {
      html += `    <!-- [${scriptCounter}] Page-specific: ${mainPageScript} | Strategy: defer -->\n`;
      html += `    <script src="scripts/${mainPageScript}?v=1.0.0" defer></script> <!-- Main page script -->\n\n`;
      loadedScripts.add(mainPageScript);
      scriptCounter++;
    }
  }
  
  // Add other page-specific scripts that are not in packages
  // NOTE: When using bundles, these scripts are included in bundles and should not be loaded separately
  const pageSpecificScripts = shouldUseBundles ? [] : [
    `${pageName}-manager.js`,
    `${pageName}-selector.js`,
    `${pageName}-renderer.js`,
    `${pageName}-integration.js`,
    `${pageName}-export-service.js`
  ];
  
  pageSpecificScripts.forEach(scriptFile => {
    if (!loadedScripts.has(scriptFile)) {
      const scriptPath = path.join(__dirname, '..', scriptFile);
      if (fs.existsSync(scriptPath)) {
        html += `    <!-- [${scriptCounter}] Page-specific: ${scriptFile} | Strategy: defer -->\n`;
        html += `    <script src="scripts/${scriptFile}?v=1.0.0" defer></script> <!-- Page-specific script -->\n\n`;
        scriptCounter++;
      }
    }
  });
  
  // Special handling for ai-analysis
  // NOTE: When using bundles, these scripts are included in bundles and should not be loaded separately
  if (pageName === 'ai-analysis' && !shouldUseBundles) {
    const aiScripts = [
      'ai-analysis-manager.js',
      'ai-template-selector.js',
      'ai-result-renderer.js',
      'ai-notes-integration.js',
      'ai-export-service.js'
    ];
    
    aiScripts.forEach(scriptFile => {
      if (!loadedScripts.has(scriptFile)) {
        const scriptPath = path.join(__dirname, '..', scriptFile);
        if (fs.existsSync(scriptPath)) {
          html += `    <!-- [${scriptCounter}] Page-specific: ${scriptFile} | Strategy: defer -->\n`;
          html += `    <script src="scripts/${scriptFile}?v=1.0.0" defer></script> <!-- Page-specific script -->\n\n`;
          scriptCounter++;
        }
      }
    });
  }
  
  html += '    <!-- =============================================================== -->\n';
  html += '    <!-- ===== FINAL SUMMARY ===== -->\n';
  html += '    <!-- =============================================================== -->\n';
  html += `    <!-- 📊 Total Scripts Processed: ${scriptCounter - 1} | Unique Scripts Loaded: ${scriptCounter - 1} -->\n`;
  html += `    <!-- 🔧 For maintenance: Use generate-script-loading-code.js -->\n`;
  html += '\n\n</body>\n</html>\n';

  return html;
}

// Get arguments from command line
const pageName = process.argv[2];
const modeArg = process.argv.find(arg => arg.startsWith('--mode='));
const useBundlesArg = process.argv.find(arg => arg === '--use-bundles' || arg === '--bundles');
const noBundlesArg = process.argv.find(arg => arg === '--no-bundles');

// Parse mode - auto-detect if not provided
let mode = modeArg ? modeArg.split('=')[1] : null;
if (!mode) {
  mode = detectEnvironment();
}

// Parse useBundles - auto-set based on mode if not provided
let useBundles = null;
if (noBundlesArg !== undefined) {
  useBundles = false;
} else if (useBundlesArg !== undefined) {
  useBundles = true;
} else if (process.env.USE_BUNDLES === 'true') {
  useBundles = true;
} else if (process.env.USE_BUNDLES === 'false') {
  useBundles = false;
} else {
  // Auto-set based on mode
  // Production and testing use bundles, development doesn't
  useBundles = (mode === 'production' || mode === 'testing');
}

if (pageName) {
  // Generate for specific page
  const code = generateScriptLoadingCode(pageName, mode, useBundles);
  if (code) {
    console.log(code);
  }
} else {
  // List all pages
  console.log('Available pages:');
  Object.keys(PAGE_CONFIGS).forEach(page => {
    console.log(`  - ${page}`);
  });
  console.log('\nUsage:');
  console.log('  node generate-script-loading-code.js <pageName> [--mode=development|production] [--use-bundles|--no-bundles]');
  console.log('\nEnvironment Detection:');
  console.log('  - TikTrackApp-Production → production (with bundles)');
  console.log('  - TikTrackApp → development (without bundles)');
  console.log('  - Use --mode flag to override auto-detection');
}

module.exports = { generateScriptLoadingCode };

