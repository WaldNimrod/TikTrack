/**
 * Generate Script Loading Code for Pages
 * Uses package-manifest.js and page-initialization-configs.js to generate correct script loading order
 */

const fs = require('fs');
const path = require('path');

// Load package manifest
const manifestPath = path.join(__dirname, 'init-system', 'package-manifest.js');
let PACKAGE_MANIFEST = {};

try {
  // Try to require the module
  const manifestModule = require(manifestPath);
  if (manifestModule && manifestModule.PACKAGE_MANIFEST) {
    PACKAGE_MANIFEST = manifestModule.PACKAGE_MANIFEST;
  }
} catch (e) {
  console.error('⚠️ Could not load package manifest via require, trying eval...');
  // Fallback to eval
  const manifestContent = fs.readFileSync(manifestPath, 'utf8');
  // Create a safe context
  const vm = require('vm');
  const context = { module: {}, exports: {}, require: require, __dirname, __filename: manifestPath };
  vm.createContext(context);
  vm.runInContext(manifestContent, context);
  if (context.module.exports && context.module.exports.PACKAGE_MANIFEST) {
    PACKAGE_MANIFEST = context.module.exports.PACKAGE_MANIFEST;
  }
}

// Load page configs - this file doesn't export, so we need to parse it
const configsPath = path.join(__dirname, 'page-initialization-configs.js');
let PAGE_CONFIGS = {};

try {
  const configsContent = fs.readFileSync(configsPath, 'utf8');
  // Extract PAGE_CONFIGS from the file
  const vm = require('vm');
  const context = { window: {}, PAGE_CONFIGS: {}, module: {}, exports: {}, require: require, __dirname, __filename: configsPath };
  vm.createContext(context);
  vm.runInContext(configsContent, context);
  if (context.PAGE_CONFIGS && Object.keys(context.PAGE_CONFIGS).length > 0) {
    PAGE_CONFIGS = context.PAGE_CONFIGS;
  } else if (context.window && context.window.PAGE_CONFIGS) {
    PAGE_CONFIGS = context.window.PAGE_CONFIGS;
  }
} catch (e) {
  console.error('⚠️ Could not load page configs:', e.message);
}

/**
 * Generate script loading code for a page
 */
function generateScriptLoadingCode(pageName) {
  const pageConfig = PAGE_CONFIGS[pageName];
  if (!pageConfig) {
    console.error(`❌ No config found for page: ${pageName}`);
    return null;
  }

  const packages = pageConfig.packages || ['base'];
  
  let html = '';
  html += '    <!-- =============================================================== -->\n';
  html += '    <!-- ===== START SCRIPT LOADING ORDER ===== -->\n';
  html += '    <!-- =============================================================== -->\n';
  html += '    <!-- ⚠️ DO NOT MODIFY MANUALLY - Generated by generate-script-loading-code.js -->\n';
  html += '    <!-- 📋 Loading Order: BASE → SERVICES → MODULES → UI-ADVANCED → CRUD → PREFERENCES → INIT-SYSTEM -->\n';
  html += `    <!-- 🎯 Page: ${pageName} | Generated: ${new Date().toISOString()} -->\n`;
  html += `    <!-- 📦 Packages: ${packages.join(', ')} -->\n\n`;
  
  const sortedPackages = packages
    .map(pkgName => PACKAGE_MANIFEST[pkgName])
    .filter(pkg => pkg)
    .sort((a, b) => (a.loadOrder || 0) - (b.loadOrder || 0));
  
  let scriptCounter = 1;
  const loadedScripts = new Set();
  
  sortedPackages.forEach((pkg, pkgIndex) => {
    html += `    <!-- =============================================================== -->\n`;
    html += `    <!-- ===== PACKAGE ${pkgIndex + 1}/${sortedPackages.length}: ${pkg.name.toUpperCase()} ===== -->\n`;
    html += `    <!-- =============================================================== -->\n`;
    html += `    <!-- Load Order: ${pkg.loadOrder} | Description: ${pkg.description} -->\n`;
    html += `    <!-- Dependencies: ${pkg.dependencies ? pkg.dependencies.join(', ') : 'none'} -->\n`;
    html += `    <!-- Critical: ${pkg.critical ? 'YES' : 'NO'} | Version: ${pkg.version} -->\n\n`;
    
    // Sort scripts within package by loadOrder
    const sortedScripts = (pkg.scripts || [])
      .filter(script => script.required !== false)
      .sort((a, b) => (a.loadOrder || 0) - (b.loadOrder || 0));
    
    sortedScripts.forEach((script) => {
      html += `    <!-- [${scriptCounter}] Load Order: ${scriptCounter} -->\n`;
      // Handle external URLs (CDN) vs local files
      const scriptSrc = script.file.startsWith('http://') || script.file.startsWith('https://') 
        ? script.file 
        : `scripts/${script.file}?v=1.0.0`;
      html += `    <script src="${scriptSrc}"></script> <!-- ${script.description} -->\n`;
      if (!(script.file.startsWith('http://') || script.file.startsWith('https://'))) {
        loadedScripts.add(script.file);
      }
      scriptCounter++;
    });
    html += '\n';
  });
  
  html += '    <!-- =============================================================== -->\n';
  html += '    <!-- ===== PAGE-SPECIFIC SCRIPTS ===== -->\n';
  html += '    <!-- =============================================================== -->\n\n';
  
  // Add page-specific scripts that are not in packages
  const pageSpecificScripts = [
    `${pageName}-manager.js`,
    `${pageName}-selector.js`,
    `${pageName}-renderer.js`,
    `${pageName}-integration.js`,
    `${pageName}-export-service.js`
  ];
  
  pageSpecificScripts.forEach(scriptFile => {
    if (!loadedScripts.has(scriptFile)) {
      const scriptPath = path.join(__dirname, '..', scriptFile);
      if (fs.existsSync(scriptPath)) {
        html += `    <!-- [${scriptCounter}] Page-specific: ${scriptFile} -->\n`;
        html += `    <script src="scripts/${scriptFile}?v=1.0.0"></script> <!-- Page-specific script -->\n\n`;
        scriptCounter++;
      }
    }
  });
  
  // Special handling for ai-analysis
  if (pageName === 'ai-analysis') {
    const aiScripts = [
      'ai-analysis-manager.js',
      'ai-template-selector.js',
      'ai-result-renderer.js',
      'ai-notes-integration.js',
      'ai-export-service.js'
    ];
    
    aiScripts.forEach(scriptFile => {
      if (!loadedScripts.has(scriptFile)) {
        const scriptPath = path.join(__dirname, '..', scriptFile);
        if (fs.existsSync(scriptPath)) {
          html += `    <!-- [${scriptCounter}] Page-specific: ${scriptFile} -->\n`;
          html += `    <script src="scripts/${scriptFile}?v=1.0.0"></script> <!-- Page-specific script -->\n\n`;
          scriptCounter++;
        }
      }
    });
  }
  
  html += '    <!-- =============================================================== -->\n';
  html += '    <!-- ===== FINAL SUMMARY ===== -->\n';
  html += '    <!-- =============================================================== -->\n';
  html += `    <!-- 📊 Total Scripts Processed: ${scriptCounter - 1} | Unique Scripts Loaded: ${scriptCounter - 1} -->\n`;
  html += `    <!-- 🔧 For maintenance: Use generate-script-loading-code.js -->\n`;
  
  return html;
}

// Get page name from command line or process all pages
const pageName = process.argv[2];

if (pageName) {
  // Generate for specific page
  const code = generateScriptLoadingCode(pageName);
  if (code) {
    console.log(code);
  }
} else {
  // List all pages
  console.log('Available pages:');
  Object.keys(PAGE_CONFIGS).forEach(page => {
    console.log(`  - ${page}`);
  });
}

module.exports = { generateScriptLoadingCode };

