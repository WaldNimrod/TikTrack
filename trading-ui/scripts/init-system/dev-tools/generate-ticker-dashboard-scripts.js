/**
 * Generate Script Tags for ticker-dashboard page
 * 
 * This script generates the correct script loading order for ticker-dashboard.html
 * based on the package manifest and page-initialization-configs.js
 * 
 * Usage: Load this script in browser console, then run:
 *   generateTickerDashboardScripts()
 */

(function() {
  'use strict';

  /**
   * Generate script tags for ticker-dashboard page
   */
  function generateTickerDashboardScripts() {
    // Packages for ticker-dashboard (from page-initialization-configs.js)
    const packages = [
      'base',
      'services',
      'ui-advanced',
      'modules',
      'crud',
      'preferences',
      'external-data',
      'entity-services',
      'entity-details',
      'info-summary',
      'init-system'
    ];

    if (!window.PACKAGE_MANIFEST) {
      console.error('❌ PACKAGE_MANIFEST not available. Please load package-manifest.js first.');
      return null;
    }

    // Sort packages by loadOrder
    const sortedPackages = packages
      .map(pkgName => window.PACKAGE_MANIFEST[pkgName])
      .filter(pkg => pkg)
      .sort((a, b) => (a.loadOrder || 0) - (b.loadOrder || 0));

    let html = '';
    let scriptCounter = 1;
    const loadedScripts = new Set();

    // Header comment
    html += '    <!-- =============================================================== -->\n';
    html += '    <!-- ===== SCRIPT LOADING ORDER - GENERATED BY generateTickerDashboardScripts() ===== -->\n';
    html += '    <!-- =============================================================== -->\n';
    html += '    <!-- ⚠️ DO NOT MODIFY MANUALLY - Use generateTickerDashboardScripts() to regenerate -->\n';
    html += '    <!-- 📋 Loading Order: BASE → SERVICES → MODULES → UI-ADVANCED → CRUD → PREFERENCES → INIT-SYSTEM -->\n';
    html += `    <!-- 🎯 Page: ticker-dashboard | Generated: ${new Date().toISOString()} -->\n`;
    html += `    <!-- 📦 Packages: ${packages.join(', ')} -->\n\n`;

    sortedPackages.forEach((pkg, pkgIndex) => {
      html += `    <!-- =============================================================== -->\n`;
      html += `    <!-- ===== PACKAGE ${pkgIndex + 1}/${sortedPackages.length}: ${pkg.name.toUpperCase()} ===== -->\n`;
      html += `    <!-- =============================================================== -->\n`;
      html += `    <!-- Load Order: ${pkg.loadOrder} | Description: ${pkg.description} -->\n`;
      html += `    <!-- Dependencies: ${pkg.dependencies ? pkg.dependencies.join(', ') : 'none'} -->\n`;
      html += `    <!-- Critical: ${pkg.critical ? 'YES' : 'NO'} | Version: ${pkg.version} -->\n\n`;

      // Sort scripts within package by loadOrder
      const sortedScripts = pkg.scripts
        .filter(script => script.required !== false)
        .sort((a, b) => (a.loadOrder || 0) - (b.loadOrder || 0));

      sortedScripts.forEach((script, scriptIndex) => {
        html += `    <!-- [${scriptCounter}] Load Order: ${script.loadOrder || scriptCounter} -->\n`;
        
        // Handle external URLs (CDN) vs local files
        const scriptSrc = script.file.startsWith('http://') || script.file.startsWith('https://') 
          ? script.file 
          : `scripts/${script.file}?v=1.0.0`;
        
        html += `    <script src="${scriptSrc}"></script> <!-- ${script.description} -->\n`;
        
        if (!(script.file.startsWith('http://') || script.file.startsWith('https://'))) {
          loadedScripts.add(script.file);
        }
        scriptCounter++;
      });
      html += '\n';
    });

    // Page-specific scripts
    html += '    <!-- =============================================================== -->\n';
    html += '    <!-- ===== PAGE-SPECIFIC SCRIPTS ===== -->\n';
    html += '    <!-- =============================================================== -->\n\n';
    
    const pageScripts = [
      'services/ticker-dashboard-data.js',
      'ticker-dashboard.js'
    ];

    pageScripts.forEach(scriptFile => {
      if (!loadedScripts.has(scriptFile)) {
        html += `    <!-- [${scriptCounter}] Page-specific: ${scriptFile} -->\n`;
        html += `    <script src="scripts/${scriptFile}?v=1.0.0"></script> <!-- Page-specific script -->\n\n`;
        scriptCounter++;
      } else {
        html += `    <!-- ⚠️ Page-specific script "${scriptFile}" already loaded via selected packages -->\n\n`;
      }
    });

    // Footer comment
    html += '    <!-- =============================================================== -->\n';
    html += '    <!-- ===== FINAL SUMMARY ===== -->\n';
    html += '    <!-- =============================================================== -->\n';
    html += `    <!-- 📊 Total Scripts Processed: ${scriptCounter - 1} | Unique Scripts Loaded: ${scriptCounter - 1} -->\n`;
    html += `    <!-- 🔧 For maintenance: Use generateTickerDashboardScripts() to regenerate -->\n`;

    return html;
  }

  // Export to window
  window.generateTickerDashboardScripts = generateTickerDashboardScripts;

  console.log('✅ generateTickerDashboardScripts() function loaded');
  console.log('📝 Usage: Run generateTickerDashboardScripts() in console to generate script tags');
})();



